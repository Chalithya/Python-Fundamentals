# ============================================================================
# FILENAME: 01_hello_world.py
# DESCRIPTION: A simple "Hello, World!" program demonstrating the basics of Python
# ============================================================================

# ----------------------------------------------------------------------------
# 1. Basic Example - The simplest Python program
# ----------------------------------------------------------------------------

# This is a comment - Python ignores everything after the # symbol
# The print function displays text to the console
print("Hello, World!")  # Output: Hello, World!

# ----------------------------------------------------------------------------
# 2. Multiple Print Statements
# ----------------------------------------------------------------------------

# You can have multiple print statements, each outputs to a new line
print("Welcome to Python!")
print("Python is fun and easy to learn.")

# ----------------------------------------------------------------------------
# 3. Print with different quote styles
# ----------------------------------------------------------------------------

# Python allows strings to be defined with single quotes
print('This string uses single quotes')

# Or double quotes
print("This string uses double quotes")

# Triple quotes allow multi-line strings
print("""This is a multi-line string.
It spans multiple lines,
and preserves whitespace.""")

# ----------------------------------------------------------------------------
# 4. Running your first program
# ----------------------------------------------------------------------------

# To run this program:
# 1. Save this file as '01_hello_world.py'
# 2. Open a terminal/command prompt
# 3. Navigate to the directory containing this file
# 4. Type: python 01_hello_world.py (or python3 01_hello_world.py)
# 5. Press Enter

# ----------------------------------------------------------------------------
